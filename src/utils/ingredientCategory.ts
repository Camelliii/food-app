/**
 * 根据食材名称获取分类
 * @param ingredientName 食材名称
 * @returns 分类，如果不在列表中返回'其他'
 */
export function getIngredientCategory(ingredientName: string): IngredientCategory {
  // 去除首尾空格
  const name = ingredientName.trim();
  
  // 完全匹配
  if (ingredientCategoryMap[name]) {
    return ingredientCategoryMap[name];
  }
  
  // 如果不在列表中，返回'其他'
  return '其他';
}

